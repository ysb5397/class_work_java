package ch02;

public class DataType3 {
    public static void main(String[] args) {

        // 데이터 타입 - 실수형
        // 수학적으로 정수가 아닌 숫자가 실수
        // 예를 들어 3.14, 0.5, 1.55 등등

        // float, double

        // L value = R value
        float floatBox1 = 0.5f; // 접미사를 붙여서 우측 값을 float값으로 정의
        double doubleBox1 = 0.123;
        // 실수형 기본 연산 단위는 double

        // R value 기본적인 연산 단위가 있다.

        long a = 2200000000L; // 정수형 기본 연산 단위는 int

        // 어째서 실수형은 4바이트가 아닌 8바이트 double을 연산 단위로 쓸까
        // 더 자세하게 쓸 수 있는 형태가 double이기 때문
    } // end of main
} // end of class
