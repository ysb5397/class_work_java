package ch03;

public class IfTest2 {
    public static void main(String[] args) {

        int age = 30;
        // if else 구문 -> 반드시 하나는 수행이 된다.
        if (age >= 20) {
            System.out.println("성인입니다.");
        } else {
            System.out.println("미성년자 입니다.");
        }

    } // end of main
} // end of class
