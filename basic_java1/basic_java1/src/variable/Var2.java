package variable;

public class Var2 {

    // 코드의 진입점 - main 함수
    public static void main(String[] args) {
        // 변수 선언 -> 이때 선언한 변수의 이름은 a
        int a;
        // 값을 넣다(초기화 했다)
        a = 10;

        //변수 b를 50으로 선언
        int b = 50;

        //변수 사용법 - 변수에 이름을 적기만 하면 된다.
        System.out.println(b);
        System.out.println(b);
        System.out.println(b);

    } // end of main
} // end of class
